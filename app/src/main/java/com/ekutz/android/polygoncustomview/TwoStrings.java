public class TwoStrings {
    String index;
    String describer;

    public TwoStrings(String index, String describer) {
        this.index = index;
        this.describer = describer;
    }

    public String getIndex() {
        return index;
    }

    public String getDescriber() {
        return describer;
    }
}
